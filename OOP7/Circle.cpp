# include "Circle.h"

// Constructor 
///////////////////////////////////////////////////////////////////////////////
Circle::Circle(double r) : radius(r) {}

// GetName function
///////////////////////////////////////////////////////////////////////////////
std::string Circle::GetName() const {
    return "Circle";
}

// Area function
///////////////////////////////////////////////////////////////////////////////
double Circle::Area() const {
    return M_PI * radius * radius;
}

// GetRadius function
////////////////////////////////////////////////////////////////////////////////
double Circle::GetRadius() const {
    return radius;
}
