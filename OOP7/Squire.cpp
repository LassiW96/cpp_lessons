# include "Squire.h"

// Constructor 
///////////////////////////////////////////////////////////////////////////////
Squire::Squire(double s) : side(s) {}

// GetName function
///////////////////////////////////////////////////////////////////////////////
std::string Squire::GetName() const {
    return "Squire";
}

// Area function
///////////////////////////////////////////////////////////////////////////////
double Squire::Area() const {
    return side * side;
}

// GetSide function
////////////////////////////////////////////////////////////////////////////////
double Squire::GetSide() const {
    return side;
}
